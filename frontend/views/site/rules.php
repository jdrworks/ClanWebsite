    <h2>Ranking</h2>
    <p>Recruit, Corporal, Sergeant, Lieutenant, Captain, General are awarded based on a point system. Points are awarded for the following:</p>
    <ul>
        <li>Capping at the Citadel</li>
        <li>Participating in events</li>
        <li>Hosting events (whether or not points are awarded will be at the discretion of the Coordinator+ facilitating the event).</li>
    </ul>
    <p>Points can also be deducted for:</p>
    <ul>
        <li>Inactivity</li>
        <li>Rule breaking</li>
    </ul>
    <p>Clan leaders will meet once a month to review members’ points. Those who have enough points for the next rank will be promoted. Any who have fewer points than their current rank will be demoted. Anyone who has been inactive for too long, or proves to be too problematic will be considered for removal from the clan.</p>
    <p>During this meeting leadership will also consider clan members for promotion to Admin, Organiser, and Coordinator, Overseer, and Deputy Owner with the following criteria:</p>
    <ul>
        <li>Admins may be nominated by an Admin or above and are expected to help moderate the clan. They can mute troublesome users and kick guests. All disciplinary actions should be screenshot and added to discord, and clan leaders should be notified (in the discord admin chat). Leaders will follow up with the problematic users for warning/demotion/removal.</li>
        <li>Organisers may be nominated by an Overseer or above and are expected to perform the same duties as Admins as well as help clan members who have questions or need assistance when possible. </li>
        <li>Coordinators may be nominated by an Overseer or above and are expected to perform the same duties as Admins and Organisers as well as host events and help facilitate events hosted by other clan members.</li>
        <li>Overseers may be nominated by a Deputy Owner or the Owner and in addition to the duties of Admins, Organisers, and Coordinators are responsible for administering the clan, settling disputes, etc. They are expected to represent the clan and set a good example.</li>
        <li>Deputy Owners may be nominated by a Deputy Owner or the Owner and have all the same duties as Admins+ and also help the owner manage the clan.</li>
    </ul>
    <h2>Event Criteria</h2>
    <p>Events can be hosted by anyone but need approval by a Coordinator or above. Once approved, a Coordinator+ will work with the clan member to organize and run the event with the following guidelines:</p>
    <ul>
        <li>Event rules and information should be posted to discord in advance, we recommend a week's notice if possible.</li>
        <li>Events should be structured to allow as many people as possible to participate. Obviously some content will be too high level for some people, but you should be willing to teach and help anyone who wants to participate.</li>
        <li>When posting event times, please include game (UTC) time to avoid confusion.</li>
        <li>Be sure you are able to commit to hosting the event. We understand emergencies happen, however you should schedule around the event.</li>
        <li>It may be a good idea to run a poll for when to host the event to gauge interest and get the best time slot for the most people.</li>
    </ul>
